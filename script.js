function GetQueryParams(qs) {
    qs = qs.split("+").join(" ");
    var params = {},
        tokens,
        re = /[?&]?([^=]+)=([^&]*)/g;
    while ((tokens = re.exec(qs))) {
        params[decodeURIComponent(tokens[1])] = decodeURIComponent(tokens[2]);
    }
    return params;
}

function SetupBackground() {
    // Tiny starfield with subtle parallax & twinkle — tuned for perf
    const canvas = document.getElementById("BackgroundStars");
    const ctx = canvas.getContext("2d", { alpha: true });
    let dpr = Math.max(1, Math.min(2, window.devicePixelRatio || 1));
    let stars = [];
    let w = 0,
        h = 0;
    let mouseX = 0,
        mouseY = 0; // for gentle parallax

    const STAR_COUNT = 220; // adjust density here

    function resize() {
        w = canvas.clientWidth;
        h = canvas.clientHeight;
        canvas.width = Math.floor(w * dpr);
        canvas.height = Math.floor(h * dpr);
        ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    }

    function rand(a, b) {
        return a + Math.random() * (b - a);
    }

    function createStars() {
        stars = new Array(STAR_COUNT).fill(0).map(() => ({
            x: Math.random() * w,
            y: Math.random() * h,
            r: rand(0.4, 1.6),
            a: rand(0.3, 0.9), // base alpha
            tw: rand(0.002, 0.008), // twinkle speed
            phase: Math.random() * Math.PI * 2,
            z: Math.random() * 0.6 + 0.4, // depth for parallax
        }));
    }

    function draw() {
        ctx.clearRect(0, 0, w, h);
        const px = (mouseX - w / 2) * 0.002;
        const py = (mouseY - h / 2) * 0.002;
        for (const s of stars) {
            const twinkle = (Math.sin(performance.now() * s.tw + s.phase) + 1) / 2; // 0..1
            const alpha = s.a * (0.6 + 0.4 * twinkle);
            const sx = s.x + px * (1 - s.z) * 50; // gentle parallax
            const sy = s.y + py * (1 - s.z) * 50;

            // soft star
            const grad = ctx.createRadialGradient(sx, sy, 0, sx, sy, 2.5 * s.r);
            grad.addColorStop(0, `hsla(260, 80%, 80%, ${alpha})`);
            grad.addColorStop(1, "hsla(260, 80%, 80%, 0)");
            ctx.fillStyle = grad;
            ctx.beginPath();
            ctx.arc(sx, sy, 2.5 * s.r, 0, Math.PI * 2);
            ctx.fill();
        }
        requestAnimationFrame(draw);
    }

    function onMove(e) {
        if (e.touches && e.touches[0]) {
            mouseX = e.touches[0].clientX;
            mouseY = e.touches[0].clientY;
        } else {
            mouseX = e.clientX;
            mouseY = e.clientY;
        }
    }

    window.addEventListener("resize", () => {
        resize();
        createStars();
    });
    window.addEventListener("mousemove", onMove, { passive: true });
    window.addEventListener("touchmove", onMove, { passive: true });

    resize();
    createStars();
    draw();
}

function UpdateContent(ContentData) {
    var ContentElements = document.querySelectorAll("[ContentID]");

    // Update Dynamic Content
    for (var Elem of ContentElements) {
        const ContentID = Elem.getAttribute("ContentID");
        if (ContentID in ContentData) {
            Elem.innerText = ContentData[ContentID];
        }
    }

    // Update Copyright Year
    document.getElementById("CopyrightYear").textContent = new Date().getFullYear();
}

function InitializeContent() {
    const Query = GetQueryParams(document.location.search);
    UpdateContent(Query?.Lang?.toLowerCase() == "eng" ? Content_ENG : Content_GER);

    if ("Name" in Query) {
        document.getElementById("HeaderTitle").innerText = Query.Name;
    }
}

function TranslateContent() {
    var LangSwitch = document.getElementsByClassName("LangSwitch");
    for (var Item of LangSwitch) {
        Item.classList.remove("LangSwitchStop");
        Item.classList.add("LangSwitchStart");
    }

    setTimeout(() => {
        const Query = GetQueryParams(document.location.search);
        var Lang = Query?.Lang?.toLowerCase() == "eng" ? "GER" : "ENG";

        if ("URLSearchParams" in window) {
            const url = new URL(window.location);
            url.searchParams.set("Lang", Lang);
            history.pushState(null, "", url);
        }

        UpdateContent(Lang == "ENG" ? Content_ENG : Content_GER);

        var LangSwitch = document.getElementsByClassName("LangSwitch");
        for (var Item of LangSwitch) {
            Item.classList.remove("LangSwitchStart");
            Item.classList.add("LangSwitchStop");
        }
    }, 150);
}
